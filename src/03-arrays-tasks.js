/**
 * Returns the sum of all items in the specified array of numbers
 *
 * @param {array} arr
 * @return {number}
 *
 * @example
 *   [] => 0
 *   [ 1, 2, 3 ]           => 6
 *   [ -1, 1, -1, 1 ]      => 0
 *   [ 1, 10, 100, 1000 ]  => 1111
 */
function getItemsSum(arr) {
  const reducer = (previousValue, currentValue) => previousValue + currentValue;
  if (arr.length === 0) {
    return '0';
  }
  return arr.reduce(reducer);
}
